const uint8_t* math_symbols_get_glyph(char c) {
    if (c >= '0' && c <= '9') {
        return GLYPH_DIGITS[c - '0'];
    }
    
    // Custom mappings for specific math glyph symbols or uppercase indicator letters
    switch (c) {
        case 'I': // Integral indicator or placeholder
        case 0xF4: // Alternate integral char code
            return GLYPH_INTEGRAL;
        case 'S': // Summation indicator or placeholder
        case 0xE4:
            return GLYPH_SUMMATION;
        case 'P': // Pi symbol representation code
        case 0xE3:
            return GLYPH_PI_8;
        case 'T': // Theta symbol
        case 0xE9:
            return GLYPH_THETA;
        case 'F': // Infinity symbol
        case 0xEC:
            return GLYPH_INFINITY;
        case 'R': // Square root symbol
        case 0xFB:
            return GLYPH_ROOT;
        default:
            return GLYPH_FALLBACK;
    }
}
